package Inheritance;

public class Players {
	String name;
	int age;
	int best_score;
	
	String getName(String name) {
		return this.name = name;
	}
	int getAge(int age) {
		return this.age = age;
	}
	int getBest_score(int best_score) {
		return this.best_score = best_score;
	}
	

}
